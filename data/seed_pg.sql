- **攻撃**: ログイン中の利用者に、意図しないリクエストを送信させる
- **被害**: 不正な送金、設定変更、書き込み
- **対策**:
  - **CSRFトークン** による検証
  - Refererヘッダのチェック
  - SameSite属性をCookieに付与

### ディレクトリトラバーサル
- **攻撃**: 「../」等の相対パスを使い、公開されていないファイルにアクセスする
- **被害**: 機密ファイルの漏えい（設定ファイル、パスワードファイル等）
- **対策**:
  - 入力値からパス区切り文字を除去する
  - ファイルアクセス時にパスを正規化して検証する

### セッションハイジャック
- **攻撃**: 他人のセッションIDを盗み、なりすましてログイン状態でアクセスする
