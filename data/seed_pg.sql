'## 暗号化方式

### 共通鍵暗号方式（対称鍵暗号）
- 暗号化と復号に **同じ鍵** を使用する
- 代表的なアルゴリズム: **AES**（現在の主流、128/192/256ビット）、DES（旧式、非推奨）
- **長所**: 処理が高速
- **短所**: 鍵の安全な受け渡しが課題（鍵配送問題）、通信相手の数だけ鍵が必要

### 公開鍵暗号方式（非対称鍵暗号）
- **公開鍵** で暗号化し、**秘密鍵** で復号する（鍵ペア）
