  - データベースのアクセス権限を最小限にする

### クロスサイトスクリプティング（XSS）
- **攻撃**: Webサイトに悪意のあるJavaScriptを埋め込み、閲覧者のブラウザで実行させる
- **被害**: Cookie情報の窃取、セッションハイジャック、偽ページの表示
