| アプリケーションゲートウェイ | アプリケーション層でプロキシとして中継 | 詳細な制御が可能だが低速 |
| 次世代ファイアウォール（NGFW） | アプリケーション識別やユーザー識別が可能 | 従来型より高度な制御 |

### IDS / IPS
| 名称 | 機能 |
|------|------|
| **IDS（侵入検知システム）** | 不正な通信を **検知して通知** する。通信は遮断しない |
| **IPS（侵入防止システム）** | 不正な通信を **検知して自動的に遮断** する |

検知方式:
- **シグネチャ型**: 既知の攻撃パターンとのマッチングで検知（未知の攻撃は検知できない）
- **アノマリ型**: 通常の通信パターンとの差異で異常を検知（誤検知が多い場合がある）

### WAF（Web Application Firewall）
- **Webアプリケーション層** の攻撃を防御
- SQLインジェクション、XSS、CSRFなどを検知・遮断
- 通常のファイアウォール（ネットワーク層）では防げない攻撃に対応

### DMZ（非武装地帯）
- 外部公開サーバ（Web、メール、DNS）を内部ネットワークから分離して配置
- 外部→DMZは限定的に許可、DMZ→内部は原則禁止
- 万が一DMZ内のサーバが侵害されても、内部ネットワークへの影響を最小化

### VPN（仮想プライベートネットワーク）
| 種類 | 特徴 |
|------|------|
| **IPsec-VPN** | ネットワーク層で暗号化。拠点間接続に適する |
| **SSL-VPN** | アプリケーション層で暗号化。リモートアクセスに適する |

### その他の対策
- **UTM（統合脅威管理）**: FW、IDS/IPS、アンチウイルス等を1台に統合した製品
